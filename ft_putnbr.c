#include "libft.h"

void	ft_putnbr(int n)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	if ((nb / 10) > 0)
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + 48);
}
